// 1. Crear una función llamada saludar que tome un parámetro nombre y devuelva un mensaje de saludo
function saludar(nombre) {
    return `Hola, ${nombre}!`;
  }
  
  // 2. Crear una función llamada sumar que tome dos parámetros a y b, y devuelva su suma
  function sumar(a, b) {
    return a + b;
  }
  
  // 3. Llamar la función saludar con su nombre y mostrar el resultado en consola
  let mensajeSaludo = saludar("Rafa");
  console.log(mensajeSaludo); // Imprime: Hola, Juan!
  
  // 4. Llamar la función sumar con los números 5 y 7, y mostrar el resultado en la consola
  let resultadoSuma = sumar(5, 7);
  console.log(resultadoSuma); // Imprime: 12
  